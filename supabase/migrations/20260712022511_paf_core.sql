create extension if not exists pgcrypto;

create table public.paf_producers (
  id bigint generated by default as identity primary key,
  token text not null unique default encode(gen_random_bytes(18), 'hex'),
  name text not null,
  cpf text not null,
  cpf_digits text not null,
  phone text,
  address text,
  agency text,
  area_ha numeric(12, 3) not null default 0 check (area_ha >= 0),
  process_status text not null default 'INTERNALIZAR' check (process_status in ('INTERNALIZAR', 'INTERNALIZADO', 'APROVADO', 'PLANTADO', 'CANCELADO')),
  planting_year integer check (planting_year is null or planting_year between 1980 and 2100),
  designer text,
  original_row integer,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  last_report_at timestamptz
);

create table public.paf_technicians (
  id bigint generated by default as identity primary key,
  name text not null,
  phone text,
  email text,
  role text,
  region text,
  active boolean not null default true,
