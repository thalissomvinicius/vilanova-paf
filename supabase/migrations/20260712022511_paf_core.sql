create table public.paf_access_accounts (
  id bigint generated by default as identity primary key,
  name text not null,
  login text not null unique check (login ~ '^[A-Z0-9._-]{3,60}$'),
  access_code_hash text not null,
  code_hint text,
  account_type text not null check (account_type in ('ADMIN', 'PRODUTOR', 'TECNICO', 'ORGANIZACAO')),
  technician_id bigint references public.paf_technicians(id) on delete set null,
  organization text,
  active boolean not null default true,
  can_submit_reports boolean not null default false,
  can_manage_visits boolean not null default false,
  notes text,
  last_login_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.paf_access_account_producers (
  access_account_id bigint not null references public.paf_access_accounts(id) on delete cascade,
  producer_id bigint not null references public.paf_producers(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (access_account_id, producer_id)
);

create table public.paf_auth_sessions (
  id bigint generated by default as identity primary key,
  token_hash text not null unique check (length(token_hash) = 64),
  role text not null check (role in ('admin', 'access')),
  access_account_id bigint not null references public.paf_access_accounts(id) on delete cascade,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null
);

create table public.paf_login_attempts (
  attempt_key text primary key,
